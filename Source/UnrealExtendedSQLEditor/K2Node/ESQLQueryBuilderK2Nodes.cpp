{
	UEdGraphPin* TableAssetPin = CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Object, UESQLTableAsset::StaticClass(), QueryBuilderTableAssetPinName);
	TableAssetPin->PinFriendlyName = LOCTEXT("QueryBuilderTableAssetFriendlyName", "Table Asset");
	TableAssetPin->bAdvancedView = true;
	TableAssetPin->PinToolTip = LOCTEXT("QueryBuilderTableAssetTooltip", "Optional SQL table asset used to drive field-name dropdowns and typed filter value pins.").ToString();

	AllocateQueryClausePins();

	UEdGraphPin* OutputPin = CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Struct, FESQLQuerySpec::StaticStruct(), QueryBuilderOutputPinName);
	OutputPin->PinFriendlyName = LOCTEXT("QueryBuilderOutputFriendlyName", "Query Spec");
	OutputPin->PinToolTip = LOCTEXT("QueryBuilderOutputTooltip", "Structured SQL query specification built from the configured filter and sort clauses.").ToString();

	Super::AllocateDefaultPins();
	RefreshQueryClausePinTypes();
}

