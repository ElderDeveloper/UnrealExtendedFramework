	void MoveOrCopyPinToIntermediate(FKismetCompilerContext& CompilerContext, UEdGraphPin* SourcePin, UEdGraphPin* TargetPin)
	{
		if (!SourcePin || !TargetPin)
		{
			return;
		}

		if (SourcePin->LinkedTo.Num() > 0)
		{
			CompilerContext.MovePinLinksToIntermediate(*SourcePin, *TargetPin);
			return;
		}

		TargetPin->DefaultValue = SourcePin->DefaultValue;
		TargetPin->DefaultTextValue = SourcePin->DefaultTextValue;
		TargetPin->DefaultObject = SourcePin->DefaultObject;
		TargetPin->AutogeneratedDefaultValue = SourcePin->AutogeneratedDefaultValue;
	}

	bool HasExplicitValue(const UEdGraphPin* Pin)
	{
		if (!Pin)
		{
			return false;
		}

		if (Pin->LinkedTo.Num() > 0)
		{
			return true;
		}

		return !Pin->DefaultValue.IsEmpty() && Pin->DefaultValue != Pin->AutogeneratedDefaultValue;
	}

	void ConfigureArrayNodeElementType(UK2Node_MakeArray* MakeArrayNode, const UScriptStruct* ElementStruct)
	{
		if (!MakeArrayNode || !ElementStruct)
		{
			return;
		}

		if (UEdGraphPin* OutputPin = MakeArrayNode->GetOutputPin())
		{
			OutputPin->PinType.ContainerType = EPinContainerType::Array;
			OutputPin->PinType.PinCategory = UEdGraphSchema_K2::PC_Struct;
			OutputPin->PinType.PinSubCategory = NAME_None;
			OutputPin->PinType.PinSubCategoryObject = const_cast<UScriptStruct*>(ElementStruct);
		}

		for (UEdGraphPin* Pin : MakeArrayNode->Pins)
		{
			if (Pin && Pin->Direction == EGPD_Input)
			{
				Pin->PinType.ContainerType = EPinContainerType::None;
				Pin->PinType.PinCategory = UEdGraphSchema_K2::PC_Struct;
				Pin->PinType.PinSubCategory = NAME_None;
				Pin->PinType.PinSubCategoryObject = const_cast<UScriptStruct*>(ElementStruct);
			}
		}
	}

	TArray<UEdGraphPin*> CollectInputPins(UK2Node_MakeArray* MakeArrayNode)
	{
		TArray<UEdGraphPin*> InputPins;
		if (!MakeArrayNode)
		{
			return InputPins;
		}

		for (UEdGraphPin* Pin : MakeArrayNode->Pins)
		{
			if (Pin && Pin->Direction == EGPD_Input)
			{
				InputPins.Add(Pin);
			}
		}

		return InputPins;
	}

	bool TryGetLiteralFilterOperation(const UEdGraphPin* OperationPin, EESQLFilterOperation& OutOperation)
	{
		if (!OperationPin || OperationPin->LinkedTo.Num() > 0)
		{
			return false;
		}

		FString DefaultValue = OperationPin->GetDefaultAsString();
		DefaultValue.TrimStartAndEndInline();
		DefaultValue.RemoveFromStart(TEXT("\""));
		DefaultValue.RemoveFromEnd(TEXT("\""));
		if (DefaultValue.IsEmpty())
		{
			return false;
		}

		const UEnum* FilterOperationEnum = StaticEnum<EESQLFilterOperation>();
		if (FilterOperationEnum)
		{
			int64 EnumValue = FilterOperationEnum->GetValueByNameString(DefaultValue);
			if (EnumValue == INDEX_NONE)
			{
				EnumValue = FilterOperationEnum->GetValueByName(FName(*DefaultValue));
			}

			if (EnumValue == INDEX_NONE && DefaultValue.Contains(TEXT("::")))
			{
				FString UnqualifiedValue;
				DefaultValue.Split(TEXT("::"), nullptr, &UnqualifiedValue, ESearchCase::IgnoreCase, ESearchDir::FromEnd);
				EnumValue = FilterOperationEnum->GetValueByNameString(UnqualifiedValue);
			}

			if (EnumValue != INDEX_NONE)
			{
				OutOperation = static_cast<EESQLFilterOperation>(EnumValue);
				return true;
			}
		}

		int32 NumericValue = 0;
		if (FDefaultValueHelper::ParseInt(DefaultValue, NumericValue))
		{
			OutOperation = static_cast<EESQLFilterOperation>(NumericValue);
			return true;
		}

		return false;
	}
