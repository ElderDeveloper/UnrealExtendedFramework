void FESQLQueryClauseUiBase::AddClausePinsForIndex(int32 Index)
{
	UK2Node* Node = GetQueryClauseNode();
	check(Node);

	UEdGraphPin* FieldNamePin = Node->CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Name, MakeFilterFieldPinName(Index));
	FieldNamePin->PinFriendlyName = FText::Format(LOCTEXT("FilterFieldFriendlyName", "Filter {0} Field Name"), FText::AsNumber(Index + 1));
	SetPinToolTip(*FieldNamePin, LOCTEXT("FilterFieldTooltip", "Field on the SQL row struct used for this filter clause."));

	UEdGraphPin* OperationPin = Node->CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Byte, StaticEnum<EESQLFilterOperation>(), MakeFilterOperationPinName(Index));
	OperationPin->PinFriendlyName = FText::Format(LOCTEXT("FilterOperationFriendlyName", "Filter {0} Operation"), FText::AsNumber(Index + 1));
	GetDefault<UEdGraphSchema_K2>()->SetPinAutogeneratedDefaultValueBasedOnType(OperationPin);
	SetPinToolTip(*OperationPin, LOCTEXT("FilterOperationTooltip", "Comparison operation used for this filter clause."));

	UEdGraphPin* ValuePin = Node->CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Struct, FESQLBindingValue::StaticStruct(), MakeFilterValuePinName(Index));
	ValuePin->PinFriendlyName = FText::Format(LOCTEXT("FilterValueFriendlyName", "Filter {0} Value"), FText::AsNumber(Index + 1));
	SetPinToolTip(*ValuePin, LOCTEXT("FilterValueTooltip", "Primary comparison value. With a literal table asset and field selected, this pin retargets to that field type."));

	UEdGraphNode::FCreatePinParams ValuesPinParams;
	ValuesPinParams.ContainerType = EPinContainerType::Array;
	UEdGraphPin* ValuesPin = Node->CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Struct, FESQLBindingValue::StaticStruct(), MakeFilterValuesPinName(Index), ValuesPinParams);
	ValuesPin->PinFriendlyName = FText::Format(LOCTEXT("FilterValuesFriendlyName", "Filter {0} Values"), FText::AsNumber(Index + 1));
	ValuesPin->bAdvancedView = true;
	SetPinToolTip(*ValuesPin, LOCTEXT("FilterValuesTooltip", "Optional binding-value array used for multi-value operations such as IN."));
	GetDefault<UEdGraphSchema_K2>()->SetPinAutogeneratedDefaultValueBasedOnType(ValuesPin);
}

