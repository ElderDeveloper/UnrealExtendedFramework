UEdGraphPin* FESQLQueryClauseUiBase::BuildFilterPin(int32 Index, FKismetCompilerContext& CompilerContext, UEdGraph* SourceGraph)
{
	UK2Node_CallFunction* MakeFilterNode = CompilerContext.SpawnIntermediateNode<UK2Node_CallFunction>(GetQueryClauseNode(), SourceGraph);
	MakeFilterNode->FunctionReference.SetExternalMember(GET_FUNCTION_NAME_CHECKED(UESQLBlueprintBridge, MakeSQLFieldFilter), UESQLBlueprintBridge::StaticClass());
	MakeFilterNode->AllocateDefaultPins();

	MoveOrCopyPinToIntermediate(CompilerContext, FindPinChecked(MakeFilterFieldPinName(Index)), MakeFilterNode->FindPinChecked(TEXT("FieldName")));
	MoveOrCopyPinToIntermediate(CompilerContext, FindPinChecked(MakeFilterOperationPinName(Index)), MakeFilterNode->FindPinChecked(TEXT("Operation")));
	ESQLK2FieldFilterUtils::MoveValuePinToBindingPin(CompilerContext, SourceGraph, GetQueryClauseNode(), FindPinChecked(MakeFilterValuePinName(Index)), MakeFilterNode->FindPinChecked(TEXT("Value")));

	if (UEdGraphPin* ValuesPin = FindPin(MakeFilterValuesPinName(Index)); ValuesPin && HasExplicitValue(ValuesPin))
	{
		MoveOrCopyPinToIntermediate(CompilerContext, ValuesPin, MakeFilterNode->FindPinChecked(TEXT("Values")));
	}

	return MakeFilterNode->GetReturnValuePin();
}

UEdGraphPin* FESQLQueryClauseUiBase::BuildSortPin(int32 Index, FKismetCompilerContext& CompilerContext, UEdGraph* SourceGraph)
{
	UK2Node_CallFunction* MakeSortNode = CompilerContext.SpawnIntermediateNode<UK2Node_CallFunction>(GetQueryClauseNode(), SourceGraph);
	MakeSortNode->FunctionReference.SetExternalMember(GET_FUNCTION_NAME_CHECKED(UESQLBlueprintBridge, MakeSQLSortRule), UESQLBlueprintBridge::StaticClass());
	MakeSortNode->AllocateDefaultPins();

	MoveOrCopyPinToIntermediate(CompilerContext, FindPinChecked(MakeSortFieldPinName(Index)), MakeSortNode->FindPinChecked(TEXT("FieldName")));
	MoveOrCopyPinToIntermediate(CompilerContext, FindPinChecked(MakeSortAscendingPinName(Index)), MakeSortNode->FindPinChecked(TEXT("bAscending")));
	return MakeSortNode->GetReturnValuePin();
}

UEdGraphPin* FESQLQueryClauseUiBase::BuildFilterArrayPin(FKismetCompilerContext& CompilerContext, UEdGraph* SourceGraph)
{
	const int32 ClauseCount = GetQueryClauseUiState().NumFilterClauses;
	if (ClauseCount <= 0)
	{
		return nullptr;
	}

	UK2Node_MakeArray* MakeArrayNode = CompilerContext.SpawnIntermediateNode<UK2Node_MakeArray>(GetQueryClauseNode(), SourceGraph);
	MakeArrayNode->AllocateDefaultPins();
	for (int32 Index = 1; Index < ClauseCount; ++Index)
	{
		MakeArrayNode->AddInputPin();
	}
	ConfigureArrayNodeElementType(MakeArrayNode, FESQLFieldFilter::StaticStruct());

	const TArray<UEdGraphPin*> InputPins = CollectInputPins(MakeArrayNode);
	for (int32 Index = 0; Index < ClauseCount && Index < InputPins.Num(); ++Index)
	{
		if (UEdGraphPin* FilterPin = BuildFilterPin(Index, CompilerContext, SourceGraph))
		{
			FilterPin->MakeLinkTo(InputPins[Index]);
		}
	}

	return MakeArrayNode->GetOutputPin();
}

UEdGraphPin* FESQLQueryClauseUiBase::BuildSortArrayPin(FKismetCompilerContext& CompilerContext, UEdGraph* SourceGraph)
{
	const int32 ClauseCount = GetQueryClauseUiState().NumSortClauses;
	if (ClauseCount <= 0)
	{
		return nullptr;
	}

	UK2Node_MakeArray* MakeArrayNode = CompilerContext.SpawnIntermediateNode<UK2Node_MakeArray>(GetQueryClauseNode(), SourceGraph);
	MakeArrayNode->AllocateDefaultPins();
	for (int32 Index = 1; Index < ClauseCount; ++Index)
	{
		MakeArrayNode->AddInputPin();
	}
	ConfigureArrayNodeElementType(MakeArrayNode, FESQLSortRule::StaticStruct());

	const TArray<UEdGraphPin*> InputPins = CollectInputPins(MakeArrayNode);
	for (int32 Index = 0; Index < ClauseCount && Index < InputPins.Num(); ++Index)
	{
		if (UEdGraphPin* SortPin = BuildSortPin(Index, CompilerContext, SourceGraph))
		{
			SortPin->MakeLinkTo(InputPins[Index]);
		}
	}

	return MakeArrayNode->GetOutputPin();
}

UEdGraphPin* FESQLQueryClauseUiBase::BuildQuerySpecPin(FKismetCompilerContext& CompilerContext, UEdGraph* SourceGraph)
{
	UK2Node_CallFunction* MakeQuerySpecNode = CompilerContext.SpawnIntermediateNode<UK2Node_CallFunction>(GetQueryClauseNode(), SourceGraph);
	MakeQuerySpecNode->FunctionReference.SetExternalMember(GET_FUNCTION_NAME_CHECKED(UESQLBlueprintBridge, MakeSQLQuerySpec), UESQLBlueprintBridge::StaticClass());
	MakeQuerySpecNode->AllocateDefaultPins();

	if (UEdGraphPin* FiltersArrayPin = BuildFilterArrayPin(CompilerContext, SourceGraph))
	{
		FiltersArrayPin->MakeLinkTo(MakeQuerySpecNode->FindPinChecked(TEXT("Filters")));
	}

	if (UEdGraphPin* SortArrayPin = BuildSortArrayPin(CompilerContext, SourceGraph))
	{
		SortArrayPin->MakeLinkTo(MakeQuerySpecNode->FindPinChecked(TEXT("SortRules")));
	}

	MoveOrCopyPinToIntermediate(CompilerContext, FindLimitPin(), MakeQuerySpecNode->FindPinChecked(TEXT("Limit")));
	MoveOrCopyPinToIntermediate(CompilerContext, FindOffsetPin(), MakeQuerySpecNode->FindPinChecked(TEXT("Offset")));
	return MakeQuerySpecNode->GetReturnValuePin();
}

