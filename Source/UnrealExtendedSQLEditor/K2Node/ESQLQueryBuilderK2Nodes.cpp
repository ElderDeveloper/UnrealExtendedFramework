bool FESQLQueryClauseUiBase::UsesMultiValuePins(const UEdGraphPin* OperationPin)
{
	EESQLFilterOperation LiteralOperation = EESQLFilterOperation::Equal;
	return TryGetLiteralFilterOperation(OperationPin, LiteralOperation) && LiteralOperation == EESQLFilterOperation::In;
}

