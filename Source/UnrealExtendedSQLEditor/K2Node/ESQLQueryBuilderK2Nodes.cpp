FESQLQueryClauseUiBase::EClausePinKind FESQLQueryClauseUiBase::ParseClausePinName(const FName PinName, int32& OutIndex)
{
	OutIndex = INDEX_NONE;
	const FString PinNameString = PinName.ToString();

	auto MatchIndexedPrefix = [&PinNameString, &OutIndex](const TCHAR* Prefix) -> bool
	{
		const FString PrefixString(Prefix);
		if (!PinNameString.StartsWith(PrefixString))
		{
			return false;
		}

		const FString Suffix = PinNameString.RightChop(PrefixString.Len());
		if (Suffix.IsEmpty() || !Suffix.IsNumeric())
		{
			return false;
		}

		OutIndex = FCString::Atoi(*Suffix);
		return true;
	};

	if (MatchIndexedPrefix(FilterFieldPrefix))
	{
		return EClausePinKind::FilterField;
	}
	if (MatchIndexedPrefix(FilterOperationPrefix))
	{
		return EClausePinKind::FilterOperation;
	}
	if (MatchIndexedPrefix(FilterValuePrefix))
	{
		return EClausePinKind::FilterValue;
	}
	if (MatchIndexedPrefix(FilterValuesPrefix))
	{
		return EClausePinKind::FilterValues;
	}
	if (MatchIndexedPrefix(SortFieldPrefix))
	{
		return EClausePinKind::SortField;
	}
	if (MatchIndexedPrefix(SortAscendingPrefix))
	{
		return EClausePinKind::SortAscending;
	}

	return EClausePinKind::None;
}

