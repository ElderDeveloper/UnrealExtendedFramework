void UK2Node_ESQLPlayerTypedRowBridgeBase::ExpandNode(FKismetCompilerContext& CompilerContext, UEdGraph* SourceGraph)
{
	Super::ExpandNode(CompilerContext, SourceGraph);

	UEdGraphPin* PlayerDBPin = GetPlayerDBPin();
	if (!PlayerDBPin || (PlayerDBPin->LinkedTo.Num() == 0 && PlayerDBPin->DefaultObject == nullptr))
	{
		CompilerContext.MessageLog.Error(*LOCTEXT("MissingPlayerDBOnExpand", "@@ must have a Player DB Component specified.").ToString(), this);
		BreakAllNodeLinks();
		return;
	}

	UEdGraphPin* TableAssetPin = GetTableAssetPin();
	if (!TableAssetPin || (TableAssetPin->LinkedTo.Num() == 0 && TableAssetPin->DefaultObject == nullptr))
	{
		CompilerContext.MessageLog.Error(*LOCTEXT("MissingPlayerTableAssetOnExpand", "@@ must have a Table Asset specified.").ToString(), this);
		BreakAllNodeLinks();
		return;
	}

	if (!ResolveRowStructType())
	{
		CompilerContext.MessageLog.Error(*LOCTEXT("MissingPlayerRowTypeOnExpand", "@@ could not infer a typed row struct. Use a literal Table Asset or connect the row pin to a typed struct.").ToString(), this);
		BreakAllNodeLinks();
		return;
	}

	UK2Node_CallFunction* BridgeCallNode = CompilerContext.SpawnIntermediateNode<UK2Node_CallFunction>(this, SourceGraph);
	BridgeCallNode->FunctionReference.SetExternalMember(GetBridgeFunctionName(), UESQLBlueprintBridge::StaticClass());
	BridgeCallNode->AllocateDefaultPins();

	CompilerContext.MovePinLinksToIntermediate(*FindPinChecked(UEdGraphSchema_K2::PN_Execute), *BridgeCallNode->GetExecPin());
	CompilerContext.MovePinLinksToIntermediate(*FindPinChecked(UEdGraphSchema_K2::PN_Then), *BridgeCallNode->GetThenPin());

	MoveOrCopyPinToIntermediate(CompilerContext, PlayerDBPin, BridgeCallNode->FindPinChecked(ESQLPlayerTypedK2NodePins::PlayerDBComponent));
	MoveOrCopyPinToIntermediate(CompilerContext, TableAssetPin, BridgeCallNode->FindPinChecked(ESQLPlayerTypedK2NodePins::TableAsset));

	ExpandOperationPins(CompilerContext, SourceGraph, BridgeCallNode);

	UEdGraphPin* TypedRowPin = GetTypedRowPin();
	UEdGraphPin* BridgeTypedRowPin = BridgeCallNode->FindPinChecked(GetTypedRowPinName());
	BridgeTypedRowPin->PinType = TypedRowPin->PinType;
	if (GetTypedRowPinDirection() == EGPD_Output)
	{
		CompilerContext.MovePinLinksToIntermediate(*TypedRowPin, *BridgeTypedRowPin);
	}
	else
	{
		MoveOrCopyPinToIntermediate(CompilerContext, TypedRowPin, BridgeTypedRowPin);
	}

	CompilerContext.MovePinLinksToIntermediate(*GetQueryResultPin(), *BridgeCallNode->GetReturnValuePin());
	BreakAllNodeLinks();
}

