void UK2Node_ESQLPlayerRowArrayBridgeBase::EarlyValidation(FCompilerResultsLog& MessageLog) const
{
	Super::EarlyValidation(MessageLog);

	const UEdGraphPin* PlayerDBPin = GetPlayerDBPin();
	const UEdGraphPin* TableAssetPin = GetTableAssetPin();
	const UEdGraphPin* RowsPin = GetRowsPin();
	if (!PlayerDBPin || !TableAssetPin || !RowsPin)
	{
		MessageLog.Error(*LOCTEXT("MissingPlayerArrayPinsError", "Missing pins in @@").ToString(), this);
		return;
	}

	if (PlayerDBPin->LinkedTo.Num() == 0 && PlayerDBPin->DefaultObject == nullptr)
	{
		MessageLog.Error(*LOCTEXT("NoPlayerDBArrayError", "No Player DB Component specified in @@").ToString(), this);
		return;
	}

	if (TableAssetPin->LinkedTo.Num() == 0)
	{
		const UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(TableAssetPin->DefaultObject);
		if (!TableAsset)
		{
			MessageLog.Error(*LOCTEXT("NoPlayerArrayTableAssetError", "No SQL Table Asset specified in @@").ToString(), this);
			return;
		}

		if (!TableAsset->RowStruct)
		{
			MessageLog.Error(*LOCTEXT("PlayerArrayMissingRowStructError", "SQL Table Asset in @@ does not define a RowStruct").ToString(), this);
			return;
		}
	}

	if (!ResolveRowStructType())
	{
		MessageLog.Error(*LOCTEXT("UnresolvedPlayerArrayRowStructError", "@@ could not infer the output row type. Use a literal Table Asset or connect Rows to a typed struct array.").ToString(), this);
		return;
	}

	ValidateOperationPins(MessageLog);
}

