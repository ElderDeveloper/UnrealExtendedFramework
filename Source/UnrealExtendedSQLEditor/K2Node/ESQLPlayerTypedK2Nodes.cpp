bool UK2Node_ESQLPlayerTypedRowBridgeBase::IsConnectionDisallowed(const UEdGraphPin* MyPin, const UEdGraphPin* OtherPin, FString& OutReason) const
{
	if (MyPin == GetTypedRowPin() && MyPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Wildcard)
	{
		const bool bValidStruct = OtherPin->PinType.ContainerType == EPinContainerType::None
			&& (OtherPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Struct || OtherPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Wildcard);
		if (!bValidStruct)
		{
			OutReason = TEXT("Row pin must connect to a struct");
			return true;
		}
	}

	return false;
}

void UK2Node_ESQLPlayerTypedRowBridgeBase::EarlyValidation(FCompilerResultsLog& MessageLog) const
{
	Super::EarlyValidation(MessageLog);

	const UEdGraphPin* PlayerDBPin = GetPlayerDBPin();
	const UEdGraphPin* TableAssetPin = GetTableAssetPin();
	const UEdGraphPin* TypedRowPin = GetTypedRowPin();
	if (!PlayerDBPin || !TableAssetPin || !TypedRowPin)
	{
		MessageLog.Error(*LOCTEXT("MissingPlayerTypedPinsError", "Missing pins in @@").ToString(), this);
		return;
	}

	if (PlayerDBPin->LinkedTo.Num() == 0 && PlayerDBPin->DefaultObject == nullptr)
	{
		MessageLog.Error(*LOCTEXT("NoPlayerDBError", "No Player DB Component specified in @@").ToString(), this);
		return;
	}

	if (TableAssetPin->LinkedTo.Num() == 0)
	{
		const UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(TableAssetPin->DefaultObject);
		if (!TableAsset)
		{
			MessageLog.Error(*LOCTEXT("NoPlayerTableAssetError", "No SQL Table Asset specified in @@").ToString(), this);
			return;
		}

		if (!TableAsset->RowStruct)
		{
			MessageLog.Error(*LOCTEXT("PlayerMissingRowStructError", "SQL Table Asset in @@ does not define a RowStruct").ToString(), this);
			return;
		}
	}

	if (!ResolveRowStructType())
	{
		MessageLog.Error(*LOCTEXT("UnresolvedPlayerRowStructError", "@@ could not infer the row struct type. Use a literal Table Asset or connect the row pin to a typed struct.").ToString(), this);
		return;
	}

	ValidateOperationPins(MessageLog);
}

void UK2Node_ESQLPlayerTypedRowBridgeBase::PreloadRequiredAssets()
{
	if (UEdGraphPin* TableAssetPin = GetTableAssetPin())
	{
		if (UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(TableAssetPin->DefaultObject))
		{
			PreloadObject(TableAsset);
		}
	}

	Super::PreloadRequiredAssets();
}

void UK2Node_ESQLPlayerTypedRowBridgeBase::NotifyPinConnectionListChanged(UEdGraphPin* Pin)
{
	Super::NotifyPinConnectionListChanged(Pin);

	if (Pin == GetTypedRowPin())
	{
		const UEdGraphPin* TableAssetPin = GetTableAssetPin();
		const bool bTypedRowAuthority = TableAssetPin && (TableAssetPin->LinkedTo.Num() > 0 || TableAssetPin->DefaultObject == nullptr);
		if (bTypedRowAuthority)
		{
			RefreshTypedRowPinType();
		}
	}
	else if (Pin == GetTableAssetPin())
	{
		CachedNodeTitle.Clear();
		RefreshTypedRowPinType();
	}
}

UEdGraphPin* UK2Node_ESQLPlayerTypedRowBridgeBase::GetPlayerDBPin(const TArray<UEdGraphPin*>* PinsToSearch) const
{
	const TArray<UEdGraphPin*>* SearchPins = PinsToSearch ? PinsToSearch : &Pins;
	for (UEdGraphPin* Pin : *SearchPins)
	{
		if (Pin && Pin->PinName == ESQLPlayerTypedK2NodePins::PlayerDBComponent)
		{
			return Pin;
		}
	}

	return nullptr;
}

