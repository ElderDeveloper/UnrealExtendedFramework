UScriptStruct* UK2Node_ESQLPlayerRowArrayBridgeBase::ResolveRowStructType() const
{
	if (const UEdGraphPin* TableAssetPin = GetTableAssetPin())
	{
		if (TableAssetPin->LinkedTo.Num() == 0)
		{
			if (const UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(TableAssetPin->DefaultObject))
			{
				return const_cast<UScriptStruct*>(TableAsset->RowStruct);
			}
		}
	}

	if (const UEdGraphPin* RowsPin = GetRowsPin())
	{
		if (RowsPin->LinkedTo.Num() > 0)
		{
			for (const UEdGraphPin* LinkedPin : RowsPin->LinkedTo)
			{
				if (UScriptStruct* LinkedStruct = ExtractStructTypeFromArrayPin(LinkedPin))
				{
					return LinkedStruct;
				}
			}
		}

		return ExtractStructTypeFromArrayPin(RowsPin);
	}

	return nullptr;
}

