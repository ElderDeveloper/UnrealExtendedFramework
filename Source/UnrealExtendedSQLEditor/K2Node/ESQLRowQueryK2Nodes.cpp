UScriptStruct* UK2Node_ESQLRowArraySubsystemBase::ResolveRowStructType() const
{
	if (const UEdGraphPin* TableAssetPin = GetTableAssetPin())
	{
		if (TableAssetPin->LinkedTo.Num() == 0)
		{
			if (const UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(TableAssetPin->DefaultObject))
			{
				return const_cast<UScriptStruct*>(TableAsset->RowStruct);
			}
		}
	}

	if (const UEdGraphPin* RowsPin = GetRowsPin())
	{
		if (RowsPin->LinkedTo.Num() > 0)
		{
			for (const UEdGraphPin* LinkedPin : RowsPin->LinkedTo)
			{
				if (UScriptStruct* LinkedStruct = ExtractStructTypeFromArrayPin(LinkedPin))
				{
					return LinkedStruct;
				}
			}
		}

		return ExtractStructTypeFromArrayPin(RowsPin);
	}

	return nullptr;
}

void UK2Node_ESQLRowArraySubsystemBase::RefreshRowsPinType()
{
	SetRowsPinType(ResolveRowStructType());
}

void UK2Node_ESQLRowArraySubsystemBase::SetRowsPinType(UScriptStruct* NewRowStruct)
{
	UEdGraphPin* RowsPin = GetRowsPin();
	UScriptStruct* OldRowStruct = ExtractStructTypeFromArrayPin(RowsPin);
	if (OldRowStruct == NewRowStruct && RowsPin->PinType.ContainerType == EPinContainerType::Array)
	{
		return;
	}

	RowsPin->PinType.ContainerType = EPinContainerType::Array;
	RowsPin->PinType.PinCategory = NewRowStruct ? UEdGraphSchema_K2::PC_Struct : UEdGraphSchema_K2::PC_Wildcard;
	RowsPin->PinType.PinSubCategory = NAME_None;
	RowsPin->PinType.PinSubCategoryObject = NewRowStruct;
	CachedNodeTitle.Clear();
}

