bool UK2Node_ESQLRowArraySubsystemBase::IsConnectionDisallowed(const UEdGraphPin* MyPin, const UEdGraphPin* OtherPin, FString& OutReason) const
{
	if (MyPin == GetRowsPin() && MyPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Wildcard)
	{
		const bool bValidStructArray = OtherPin->PinType.ContainerType == EPinContainerType::Array
			&& (OtherPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Struct || OtherPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Wildcard);
		if (!bValidStructArray)
		{
			OutReason = TEXT("Rows must connect to an array of structs");
			return true;
		}
	}

	return false;
}

void UK2Node_ESQLRowArraySubsystemBase::EarlyValidation(FCompilerResultsLog& MessageLog) const
{
	Super::EarlyValidation(MessageLog);

	const UEdGraphPin* TableAssetPin = GetTableAssetPin();
	const UEdGraphPin* RowsPin = GetRowsPin();
	if (!TableAssetPin || !RowsPin)
	{
		MessageLog.Error(*LOCTEXT("MissingPinsError", "Missing pins in @@").ToString(), this);
		return;
	}

	if (TableAssetPin->LinkedTo.Num() == 0)
	{
		const UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(TableAssetPin->DefaultObject);
		if (!TableAsset)
		{
			MessageLog.Error(*LOCTEXT("NoTableAssetError", "No SQL Table Asset specified in @@").ToString(), this);
			return;
		}

		if (!TableAsset->RowStruct)
		{
			MessageLog.Error(*LOCTEXT("MissingRowStructError", "SQL Table Asset in @@ does not define a RowStruct").ToString(), this);
			return;
		}
	}

	if (!ResolveRowStructType())
	{
		MessageLog.Error(*LOCTEXT("UnresolvedRowStructError", "@@ could not infer the output row type. Use a literal Table Asset or connect Rows to a typed struct array.").ToString(), this);
		return;
	}

	ValidateOperationPins(MessageLog);
}

void UK2Node_ESQLRowArraySubsystemBase::PreloadRequiredAssets()
{
	if (UEdGraphPin* TableAssetPin = GetTableAssetPin())
	{
		if (UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(TableAssetPin->DefaultObject))
		{
			PreloadObject(TableAsset);
		}
	}

	Super::PreloadRequiredAssets();
}

void UK2Node_ESQLRowArraySubsystemBase::NotifyPinConnectionListChanged(UEdGraphPin* Pin)
{
	Super::NotifyPinConnectionListChanged(Pin);

	if (Pin == GetRowsPin())
	{
		const UEdGraphPin* TableAssetPin = GetTableAssetPin();
		const bool bRowsAuthority = TableAssetPin && (TableAssetPin->LinkedTo.Num() > 0 || TableAssetPin->DefaultObject == nullptr);
		if (bRowsAuthority)
		{
			RefreshRowsPinType();
		}
	}
	else if (Pin == GetTableAssetPin())
	{
		CachedNodeTitle.Clear();
		RefreshRowsPinType();
	}

	RefreshCustomPinTypes();
}

UEdGraphPin* UK2Node_ESQLRowArraySubsystemBase::GetWorldContextPin(const TArray<UEdGraphPin*>* PinsToSearch) const
{
	const TArray<UEdGraphPin*>* SearchPins = PinsToSearch ? PinsToSearch : &Pins;
	for (UEdGraphPin* Pin : *SearchPins)
	{
		if (Pin && Pin->PinName == ESQLRowQueryK2NodePins::WorldContextObject)
		{
			return Pin;
		}
	}

	return nullptr;
}

