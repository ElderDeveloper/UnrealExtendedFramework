void UK2Node_ESQLCountRows::ExpandNode(FKismetCompilerContext& CompilerContext, UEdGraph* SourceGraph)
{
	Super::ExpandNode(CompilerContext, SourceGraph);

	UEdGraphPin* TableAssetPin = GetTableAssetPin();
	if (!TableAssetPin || (TableAssetPin->LinkedTo.Num() == 0 && TableAssetPin->DefaultObject == nullptr))
	{
		CompilerContext.MessageLog.Error(*LOCTEXT("MissingCountRowsTableAssetOnExpand", "@@ must have a Table Asset specified.").ToString(), this);
		BreakAllNodeLinks();
		return;
	}

	UEdGraphPin* WorldContextPin = GetWorldContextPin();
	UK2Node_CallFunction* GetSubsystemNode = SpawnSQLSubsystemGetterNode(CompilerContext, *this, SourceGraph, WorldContextPin);

	UK2Node_CallFunction* SubsystemCallNode = CompilerContext.SpawnIntermediateNode<UK2Node_CallFunction>(this, SourceGraph);
	static const FName CountRowsByFieldFunctionName(TEXT("CountSQLRowsByField"));
	if (!ESQLRuntimeReflectionUtils::BindSQLSubsystemFunction(*SubsystemCallNode, CountRowsByFieldFunctionName))
	{
		CompilerContext.MessageLog.Error(*LOCTEXT("MissingCountRowsSubsystemFunctionOnExpand", "@@ could not resolve the runtime SQL subsystem function.").ToString(), this);
		BreakAllNodeLinks();
		return;
	}
	SubsystemCallNode->AllocateDefaultPins();

	CompilerContext.MovePinLinksToIntermediate(*FindPinChecked(UEdGraphSchema_K2::PN_Execute), *SubsystemCallNode->GetExecPin());
	CompilerContext.MovePinLinksToIntermediate(*FindPinChecked(UEdGraphSchema_K2::PN_Then), *SubsystemCallNode->GetThenPin());

	GetSubsystemNode->GetReturnValuePin()->MakeLinkTo(SubsystemCallNode->FindPinChecked(UEdGraphSchema_K2::PN_Self));

	UEdGraphPin* SubsystemTableAssetPin = SubsystemCallNode->FindPinChecked(ESQLRowQueryK2NodePins::TableAsset);
	if (TableAssetPin->LinkedTo.Num() > 0)
	{
		CompilerContext.MovePinLinksToIntermediate(*TableAssetPin, *SubsystemTableAssetPin);
	}
	else
	{
		SubsystemTableAssetPin->DefaultObject = TableAssetPin->DefaultObject;
	}

	MoveFieldFilterPinsToIntermediate(CompilerContext, SourceGraph, this, SubsystemCallNode);
	CompilerContext.MovePinLinksToIntermediate(*GetCountPin(), *SubsystemCallNode->FindPinChecked(ESQLRowQueryK2NodePins::OutCount));
	CompilerContext.MovePinLinksToIntermediate(*GetQueryResultPin(), *SubsystemCallNode->GetReturnValuePin());

	BreakAllNodeLinks();
}

