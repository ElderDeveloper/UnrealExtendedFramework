void UK2Node_ESQLRowArraySubsystemBase::AllocateDefaultPins()
{
	CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Exec, UEdGraphSchema_K2::PN_Execute);
	CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Exec, UEdGraphSchema_K2::PN_Then);

	UEdGraphPin* WorldContextPin = CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Object, UObject::StaticClass(), ESQLRowQueryK2NodePins::WorldContextObject);
	WorldContextPin->PinFriendlyName = LOCTEXT("WorldContextFriendlyName", "World Context");
	SetPinToolTip(*WorldContextPin, LOCTEXT("WorldContextTooltip", "Runtime world context used to initialize and query the SQL table asset."));

	UEdGraphPin* TableAssetPin = CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Object, UESQLTableAsset::StaticClass(), ESQLRowQueryK2NodePins::TableAsset);
	TableAssetPin->PinFriendlyName = LOCTEXT("TableAssetFriendlyName", "Table Asset");
	SetPinToolTip(*TableAssetPin, LOCTEXT("TableAssetTooltip", "The SQL table asset that owns the row struct and query behavior."));

	AllocateOperationPins();

	UEdGraphNode::FCreatePinParams RowsPinParams;
	RowsPinParams.ContainerType = EPinContainerType::Array;
	UEdGraphPin* RowsPin = CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Wildcard, ESQLRowQueryK2NodePins::OutRows, RowsPinParams);
	RowsPin->PinFriendlyName = LOCTEXT("RowsFriendlyName", "Rows");
	SetPinToolTip(*RowsPin, LOCTEXT("RowsTooltip", "Typed rows loaded from the SQL table asset."));

	UEdGraphPin* ResultPin = CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Struct, FESQLQueryResult::StaticStruct(), ESQLRowQueryK2NodePins::Result);
	ResultPin->PinFriendlyName = LOCTEXT("ResultFriendlyName", "Result");
	SetPinToolTip(*ResultPin, LOCTEXT("ResultTooltip", "Raw SQL query result for success state, rows, and error details."));

	Super::AllocateDefaultPins();
	RefreshRowsPinType();
}

FText UK2Node_ESQLRowArraySubsystemBase::GetNodeTitle(ENodeTitleType::Type TitleType) const
{
	if (TitleType == ENodeTitleType::MenuTitle)
	{
		return GetMenuTitleText();
	}

	const UEdGraphPin* TableAssetPin = GetTableAssetPin();
	if (TableAssetPin && TableAssetPin->LinkedTo.Num() == 0 && TableAssetPin->DefaultObject)
	{
		if (CachedNodeTitle.IsOutOfDate(this))
		{
			FFormatNamedArguments Args;
			Args.Add(TEXT("Title"), GetMenuTitleText());
			Args.Add(TEXT("TableName"), FText::FromString(TableAssetPin->DefaultObject->GetName()));
			CachedNodeTitle.SetCachedText(FText::Format(LOCTEXT("AssetAwareNodeTitle", "{Title} {TableName}"), Args), this);
		}

		return CachedNodeTitle;
	}

	return GetMenuTitleText();
}

