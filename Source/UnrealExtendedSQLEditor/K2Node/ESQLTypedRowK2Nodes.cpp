void UK2Node_ESQLTypedRowSubsystemBase::ExpandNode(FKismetCompilerContext& CompilerContext, UEdGraph* SourceGraph)
{
	Super::ExpandNode(CompilerContext, SourceGraph);

	UEdGraphPin* TableAssetPin = GetTableAssetPin();
	if (!TableAssetPin || (TableAssetPin->LinkedTo.Num() == 0 && TableAssetPin->DefaultObject == nullptr))
	{
		CompilerContext.MessageLog.Error(*LOCTEXT("MissingTableAssetOnExpand", "@@ must have a Table Asset specified.").ToString(), this);
		BreakAllNodeLinks();
		return;
	}

	if (!ResolveRowStructType())
	{
		CompilerContext.MessageLog.Error(*LOCTEXT("MissingRowTypeOnExpand", "@@ could not infer a typed row struct. Use a literal Table Asset or connect the row pin to a typed struct.").ToString(), this);
		BreakAllNodeLinks();
		return;
	}

	UEdGraphPin* WorldContextPin = GetWorldContextPin();
	UK2Node_CallFunction* GetSubsystemNode = SpawnSQLSubsystemGetterNode(CompilerContext, *this, SourceGraph, WorldContextPin);

	UK2Node_CallFunction* SubsystemCallNode = CompilerContext.SpawnIntermediateNode<UK2Node_CallFunction>(this, SourceGraph);
	if (!ESQLRuntimeReflectionUtils::BindSQLSubsystemFunction(*SubsystemCallNode, GetSubsystemFunctionName()))
	{
		CompilerContext.MessageLog.Error(*LOCTEXT("MissingSubsystemFunctionOnExpand", "@@ could not resolve the runtime SQL subsystem function.").ToString(), this);
		BreakAllNodeLinks();
		return;
	}
	SubsystemCallNode->AllocateDefaultPins();

	CompilerContext.MovePinLinksToIntermediate(*FindPinChecked(UEdGraphSchema_K2::PN_Execute), *SubsystemCallNode->GetExecPin());
	CompilerContext.MovePinLinksToIntermediate(*FindPinChecked(UEdGraphSchema_K2::PN_Then), *SubsystemCallNode->GetThenPin());

	GetSubsystemNode->GetReturnValuePin()->MakeLinkTo(SubsystemCallNode->FindPinChecked(UEdGraphSchema_K2::PN_Self));

	UEdGraphPin* SubsystemTableAssetPin = SubsystemCallNode->FindPinChecked(ESQLTypedRowK2NodePins::TableAsset);
	if (TableAssetPin->LinkedTo.Num() > 0)
	{
		CompilerContext.MovePinLinksToIntermediate(*TableAssetPin, *SubsystemTableAssetPin);
	}
	else
	{
		SubsystemTableAssetPin->DefaultObject = TableAssetPin->DefaultObject;
	}

	ExpandOperationPins(CompilerContext, SourceGraph, SubsystemCallNode);

	UEdGraphPin* TypedRowPin = GetTypedRowPin();
	UEdGraphPin* SubsystemTypedRowPin = SubsystemCallNode->FindPinChecked(GetTypedRowPinName());
	SubsystemTypedRowPin->PinType = TypedRowPin->PinType;
	if (GetTypedRowPinDirection() == EGPD_Output)
	{
		CompilerContext.MovePinLinksToIntermediate(*TypedRowPin, *SubsystemTypedRowPin);
	}
	else
	{
		MoveOrCopyPinToIntermediate(CompilerContext, TypedRowPin, SubsystemTypedRowPin);
	}

	CompilerContext.MovePinLinksToIntermediate(*GetQueryResultPin(), *SubsystemCallNode->GetReturnValuePin());
	BreakAllNodeLinks();
}

