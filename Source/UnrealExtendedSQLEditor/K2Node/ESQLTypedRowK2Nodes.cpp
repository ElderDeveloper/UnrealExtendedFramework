	bool ShouldRegisterBlueprintAction(const UClass* NodeClass)
	{
		return NodeClass && !NodeClass->HasMetaData(TEXT("ESQLHideFromBlueprintPalette"));
	}

	const UEdGraphPin* FindPinByName(const TArray<UEdGraphPin*>& PinsToSearch, const FName PinName)
	{
		for (const UEdGraphPin* Pin : PinsToSearch)
		{
			if (Pin && Pin->PinName == PinName)
			{
				return Pin;
			}
		}

		return nullptr;
	}

	UScriptStruct* ExtractStructTypeFromPin(const UEdGraphPin* Pin)
	{
		if (!Pin || Pin->PinType.ContainerType != EPinContainerType::None)
		{
			return nullptr;
		}

		if (Pin->PinType.PinCategory != UEdGraphSchema_K2::PC_Struct)
		{
			return nullptr;
		}

		return Cast<UScriptStruct>(Pin->PinType.PinSubCategoryObject.Get());
	}

	bool IsESQLIdStructPin(const UEdGraphPin* Pin)
	{
		const UScriptStruct* PinStruct = ExtractStructTypeFromPin(Pin);
		const UScriptStruct* SqlIdStruct = FESQLId::StaticStruct();
		return PinStruct
			&& SqlIdStruct
			&& (PinStruct == SqlIdStruct || PinStruct->GetName().Contains(SqlIdStruct->GetName(), ESearchCase::IgnoreCase));
	}

	void MoveOrCopyPinToIntermediate(FKismetCompilerContext& CompilerContext, UEdGraphPin* SourcePin, UEdGraphPin* TargetPin)
	{
		if (!SourcePin || !TargetPin)
		{
			return;
		}

		if (SourcePin->LinkedTo.Num() > 0)
		{
			CompilerContext.MovePinLinksToIntermediate(*SourcePin, *TargetPin);
			return;
		}

		TargetPin->DefaultValue = SourcePin->DefaultValue;
		TargetPin->DefaultTextValue = SourcePin->DefaultTextValue;
		TargetPin->DefaultObject = SourcePin->DefaultObject;
		TargetPin->AutogeneratedDefaultValue = SourcePin->AutogeneratedDefaultValue;
	}

	UK2Node_CallFunction* SpawnSQLSubsystemGetterNode(FKismetCompilerContext& CompilerContext, UK2Node& SourceNode, UEdGraph* SourceGraph, UEdGraphPin* WorldContextPin)
	{
		static const FName ContextObjectPinName(TEXT("ContextObject"));
		static const FName ClassPinName(TEXT("Class"));

		UK2Node_CallFunction* GetSubsystemNode = CompilerContext.SpawnIntermediateNode<UK2Node_CallFunction>(&SourceNode, SourceGraph);
		ESQLRuntimeReflectionUtils::BindGameInstanceSubsystemGetter(*GetSubsystemNode);
		GetSubsystemNode->AllocateDefaultPins();

		UEdGraphPin* GetterWorldContextPin = GetSubsystemNode->FindPinChecked(ContextObjectPinName);
		if (WorldContextPin && WorldContextPin->LinkedTo.Num() > 0)
		{
			CompilerContext.MovePinLinksToIntermediate(*WorldContextPin, *GetterWorldContextPin);
		}
		else if (WorldContextPin && WorldContextPin->DefaultObject)
		{
			GetterWorldContextPin->DefaultObject = WorldContextPin->DefaultObject;
		}
		else
		{
			UK2Node_Self* SelfNode = CompilerContext.SpawnIntermediateNode<UK2Node_Self>(&SourceNode, SourceGraph);
			SelfNode->AllocateDefaultPins();
			SelfNode->FindPinChecked(UEdGraphSchema_K2::PN_Self)->MakeLinkTo(GetterWorldContextPin);
		}

		UEdGraphPin* GetterClassPin = GetSubsystemNode->FindPinChecked(ClassPinName);
		GetterClassPin->DefaultObject = UESQLSubsystem::StaticClass();

		UEdGraphPin* GetterResultPin = GetSubsystemNode->GetReturnValuePin();
		GetterResultPin->PinType.PinCategory = UEdGraphSchema_K2::PC_Object;
		GetterResultPin->PinType.PinSubCategory = NAME_None;
		GetterResultPin->PinType.PinSubCategoryObject = UESQLSubsystem::StaticClass();
		return GetSubsystemNode;
	}

	void MoveFieldFilterPinsToIntermediate(FKismetCompilerContext& CompilerContext, UEdGraph* SourceGraph, UK2Node* SourceNode, UK2Node_CallFunction* FunctionNode)
	{
		MoveOrCopyPinToIntermediate(CompilerContext, SourceNode->FindPinChecked(ESQLTypedRowK2NodePins::FieldName), FunctionNode->FindPinChecked(ESQLTypedRowK2NodePins::FieldName));
		MoveOrCopyPinToIntermediate(CompilerContext, SourceNode->FindPinChecked(ESQLTypedRowK2NodePins::Operation), FunctionNode->FindPinChecked(ESQLTypedRowK2NodePins::Operation));
		ESQLK2FieldFilterUtils::MoveValuePinToBindingPin(CompilerContext, SourceGraph, SourceNode, SourceNode->FindPinChecked(ESQLTypedRowK2NodePins::Value), FunctionNode->FindPinChecked(ESQLTypedRowK2NodePins::Value));
	}

	void RefreshFieldValuePinType(UK2Node* Node)
	{
		if (UEdGraphPin* ValuePin = Node ? Node->FindPin(ESQLTypedRowK2NodePins::Value) : nullptr)
		{
			ESQLK2FieldFilterUtils::ApplyFieldValuePinType(*ValuePin, ESQLK2FieldFilterUtils::ResolveLiteralFieldProperty(Node, ESQLTypedRowK2NodePins::TableAsset, ESQLTypedRowK2NodePins::FieldName));
		}
	}

	bool TryGetSqlIdFromStructDefault(const UEdGraphPin* Pin, FESQLId& OutSqlId)
	{
		OutSqlId = FESQLId();
		if (!Pin
			|| Pin->LinkedTo.Num() > 0
			|| !IsESQLIdStructPin(Pin))
		{
			return false;
		}

		const FString DefaultValue = Pin->GetDefaultAsString();
		if (DefaultValue.IsEmpty())
		{
			return false;
		}

		FOutputDeviceNull NullOutput;
		FESQLId::StaticStruct()->ImportText(*DefaultValue, &OutSqlId, nullptr, PPF_SerializedAsImportText, &NullOutput, Pin->PinName.ToString());
		return !OutSqlId.Value.IsEmpty();
	}
