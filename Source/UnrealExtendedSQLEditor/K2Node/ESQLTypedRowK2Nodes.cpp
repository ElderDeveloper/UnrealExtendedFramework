UScriptStruct* UK2Node_ESQLTypedRowSubsystemBase::ResolveRowStructType() const
{
	if (const UEdGraphPin* TableAssetPin = GetTableAssetPin())
	{
		if (TableAssetPin->LinkedTo.Num() == 0)
		{
			if (const UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(TableAssetPin->DefaultObject))
			{
				return const_cast<UScriptStruct*>(TableAsset->RowStruct);
			}
		}
	}

	if (const UEdGraphPin* TypedRowPin = GetTypedRowPin())
	{
		if (TypedRowPin->LinkedTo.Num() > 0)
		{
			for (const UEdGraphPin* LinkedPin : TypedRowPin->LinkedTo)
			{
				if (UScriptStruct* LinkedStruct = ExtractStructTypeFromPin(LinkedPin))
				{
					return LinkedStruct;
				}
			}
		}

		return ExtractStructTypeFromPin(TypedRowPin);
	}

	return nullptr;
}

void UK2Node_ESQLTypedRowSubsystemBase::RefreshTypedRowPinType()
{
	SetTypedRowPinType(ResolveRowStructType());
}

void UK2Node_ESQLTypedRowSubsystemBase::SetTypedRowPinType(UScriptStruct* NewRowStruct)
{
	UEdGraphPin* TypedRowPin = GetTypedRowPin();
	UScriptStruct* OldRowStruct = ExtractStructTypeFromPin(TypedRowPin);
	if (OldRowStruct == NewRowStruct && TypedRowPin->PinType.ContainerType == EPinContainerType::None)
	{
		return;
	}

	if (TypedRowPin->SubPins.Num() > 0)
	{
		GetSchema()->RecombinePin(TypedRowPin);
	}

	TypedRowPin->PinType.ContainerType = EPinContainerType::None;
	TypedRowPin->PinType.PinCategory = NewRowStruct ? UEdGraphSchema_K2::PC_Struct : UEdGraphSchema_K2::PC_Wildcard;
	TypedRowPin->PinType.PinSubCategory = NAME_None;
	TypedRowPin->PinType.PinSubCategoryObject = NewRowStruct;
	CachedNodeTitle.Clear();
}

