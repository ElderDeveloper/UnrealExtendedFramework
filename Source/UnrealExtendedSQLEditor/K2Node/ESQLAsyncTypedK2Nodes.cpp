{
	return GetNodeTooltipText();
}

void UK2Node_ESQLAsyncTypedResultBase::PinDefaultValueChanged(UEdGraphPin* Pin)
{
	Super::PinDefaultValueChanged(Pin);

	if (Pin == GetTableAssetPin())
	{
		CachedNodeTitle.Clear();
		RefreshTypedDataPinType();
	}
}

