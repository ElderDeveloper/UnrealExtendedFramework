void UK2Node_ESQLAsyncSaveRow::ValidateNodeDuringCompilation(FCompilerResultsLog& MessageLog) const
{
	Super::ValidateNodeDuringCompilation(MessageLog);

	const UEdGraphPin* TableAssetPin = GetTableAssetPin();
	if (!TableAssetPin || TableAssetPin->LinkedTo.Num() > 0)
	{
		MessageLog.Error(*LOCTEXT("AsyncSaveTypedLiteralTableRequired", "@@ currently requires a literal SQL Table Asset so the async save input can stay typed.").ToString(), this);
		return;
	}

	const UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(TableAssetPin->DefaultObject);
	if (!TableAsset)
	{
		MessageLog.Error(*LOCTEXT("AsyncSaveMissingTable", "No SQL Table Asset specified in @@").ToString(), this);
		return;
	}

	if (!TableAsset->RowStruct)
	{
		MessageLog.Error(*LOCTEXT("AsyncSaveMissingRowStruct", "SQL Table Asset in @@ does not define a RowStruct").ToString(), this);
		return;
	}

	if (!ResolveRowStructType())
	{
		MessageLog.Error(*LOCTEXT("AsyncSaveMissingInputType", "@@ could not infer the typed row input. Use a literal SQL Table Asset or connect Row Data to a typed struct.").ToString(), this);
		return;
	}
}

UEdGraphPin* UK2Node_ESQLAsyncSaveRow::GetTableAssetPin(const TArray<UEdGraphPin*>* PinsToSearch) const
{
	const TArray<UEdGraphPin*>* SearchPins = PinsToSearch ? PinsToSearch : &Pins;
	for (UEdGraphPin* Pin : *SearchPins)
	{
		if (Pin && Pin->PinName == ESQLAsyncTypedK2NodePins::TableAsset)
		{
			return Pin;
		}
	}

	return nullptr;
}

UEdGraphPin* UK2Node_ESQLAsyncSaveRow::GetRowDataPin() const
{
	return FindPinChecked(ESQLAsyncTypedK2NodePins::RowData);
}

UScriptStruct* UK2Node_ESQLAsyncSaveRow::ResolveRowStructType() const
{
	if (const UEdGraphPin* TableAssetPin = GetTableAssetPin())
	{
		if (const UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(TableAssetPin->DefaultObject))
		{
			return const_cast<UScriptStruct*>(TableAsset->RowStruct);
		}
	}

	if (const UEdGraphPin* RowDataPin = GetRowDataPin())
	{
		if (RowDataPin->LinkedTo.Num() > 0)
		{
			for (const UEdGraphPin* LinkedPin : RowDataPin->LinkedTo)
			{
				if (UScriptStruct* LinkedStruct = ExtractStructTypeFromPin(LinkedPin))
				{
					return LinkedStruct;
				}
			}
		}

		return ExtractStructTypeFromPin(RowDataPin);
	}

	return nullptr;
}

void UK2Node_ESQLAsyncSaveRow::RefreshRowDataPinType()
{
	SetRowDataPinType(ResolveRowStructType());
}

