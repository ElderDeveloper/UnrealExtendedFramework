void UK2Node_ESQLAsyncTypedResultBase::AllocateDefaultPins()
{
	Super::AllocateDefaultPins();

	UEdGraphNode::FCreatePinParams TypedPinParams;
	TypedPinParams.ContainerType = GetTypedDataPinContainerType();
	UEdGraphPin* TypedPin = CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Wildcard, GetTypedDataPinName(), TypedPinParams);
	TypedPin->PinFriendlyName = GetTypedDataPinFriendlyName();
	SetPinToolTip(*TypedPin, GetTypedDataPinDescription());
	RefreshTypedDataPinType();
}

bool UK2Node_ESQLAsyncTypedResultBase::CanSplitPin(const UEdGraphPin* Pin) const
{
	return !IsESQLIdStructPin(Pin) && Super::CanSplitPin(Pin);
}

FText UK2Node_ESQLAsyncTypedResultBase::GetNodeTitle(ENodeTitleType::Type TitleType) const
{
	if (TitleType == ENodeTitleType::MenuTitle)
	{
		return GetMenuTitleText();
	}

	const UEdGraphPin* TableAssetPin = GetTableAssetPin();
	if (TableAssetPin && TableAssetPin->LinkedTo.Num() == 0 && TableAssetPin->DefaultObject)
	{
		if (CachedNodeTitle.IsOutOfDate(this))
		{
			FFormatNamedArguments Args;
			Args.Add(TEXT("Title"), GetMenuTitleText());
			Args.Add(TEXT("TableName"), FText::FromString(TableAssetPin->DefaultObject->GetName()));
			CachedNodeTitle.SetCachedText(FText::Format(LOCTEXT("AsyncAssetAwareNodeTitle", "{Title} {TableName}"), Args), this);
		}

		return CachedNodeTitle;
	}

	return GetMenuTitleText();
}

