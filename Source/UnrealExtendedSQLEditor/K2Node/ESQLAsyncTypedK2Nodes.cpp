bool UK2Node_ESQLAsyncTypedResultBase::IsConnectionDisallowed(const UEdGraphPin* MyPin, const UEdGraphPin* OtherPin, FString& OutReason) const
{
	if (MyPin == GetTypedDataPin() && MyPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Wildcard)
	{
		const bool bValidPin = GetTypedDataPinContainerType() == EPinContainerType::Array
			? (OtherPin->PinType.ContainerType == EPinContainerType::Array && (OtherPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Struct || OtherPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Wildcard))
			: (OtherPin->PinType.ContainerType == EPinContainerType::None && (OtherPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Struct || OtherPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Wildcard));

		if (!bValidPin)
		{
			OutReason = GetTypedDataPinContainerType() == EPinContainerType::Array
				? TEXT("Rows must connect to an array of structs")
				: TEXT("Row must connect to a struct");
			return true;
		}
	}

	return Super::IsConnectionDisallowed(MyPin, OtherPin, OutReason);
}

void UK2Node_ESQLAsyncTypedResultBase::PreloadRequiredAssets()
{
	if (UEdGraphPin* TableAssetPin = GetTableAssetPin())
	{
		if (UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(TableAssetPin->DefaultObject))
		{
			PreloadObject(TableAsset);
		}
	}

	Super::PreloadRequiredAssets();
}

void UK2Node_ESQLAsyncTypedResultBase::NotifyPinConnectionListChanged(UEdGraphPin* Pin)
{
	Super::NotifyPinConnectionListChanged(Pin);

	if (Pin == GetTypedDataPin())
	{
		RefreshTypedDataPinType();
	}
	else if (Pin == GetTableAssetPin())
	{
		CachedNodeTitle.Clear();
		RefreshTypedDataPinType();
	}
}

void UK2Node_ESQLAsyncTypedResultBase::ValidateNodeDuringCompilation(FCompilerResultsLog& MessageLog) const
{
	Super::ValidateNodeDuringCompilation(MessageLog);

	const UEdGraphPin* TableAssetPin = GetTableAssetPin();
	if (!TableAssetPin || TableAssetPin->LinkedTo.Num() > 0)
	{
		MessageLog.Error(*LOCTEXT("AsyncTypedLiteralTableRequired", "@@ currently requires a literal SQL Table Asset so the typed async result can be hydrated deterministically.").ToString(), this);
		return;
	}

	const UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(TableAssetPin->DefaultObject);
	if (!TableAsset)
	{
		MessageLog.Error(*LOCTEXT("AsyncTypedMissingTable", "No SQL Table Asset specified in @@").ToString(), this);
		return;
	}

	if (!TableAsset->RowStruct)
	{
		MessageLog.Error(*LOCTEXT("AsyncTypedMissingRowStruct", "SQL Table Asset in @@ does not define a RowStruct").ToString(), this);
		return;
	}

	if (!ResolveRowStructType())
	{
		MessageLog.Error(*LOCTEXT("AsyncTypedMissingOutputType", "@@ could not infer the typed async output. Use a literal SQL Table Asset or connect the typed output pin to a struct-compatible type.").ToString(), this);
		return;
	}
}

UEdGraphPin* UK2Node_ESQLAsyncTypedResultBase::GetTableAssetPin(const TArray<UEdGraphPin*>* PinsToSearch) const
{
	const TArray<UEdGraphPin*>* SearchPins = PinsToSearch ? PinsToSearch : &Pins;
	for (UEdGraphPin* Pin : *SearchPins)
	{
		if (Pin && Pin->PinName == ESQLAsyncTypedK2NodePins::TableAsset)
		{
			return Pin;
		}
	}

	return nullptr;
}

UEdGraphPin* UK2Node_ESQLAsyncTypedResultBase::GetTypedDataPin() const
{
	return FindPinChecked(GetTypedDataPinName());
}

UEdGraphPin* UK2Node_ESQLAsyncTypedResultBase::GetResultPin() const
{
	return FindPinChecked(ESQLAsyncTypedK2NodePins::Result);
}

UScriptStruct* UK2Node_ESQLAsyncTypedResultBase::ResolveRowStructType() const
{
	if (const UEdGraphPin* TableAssetPin = GetTableAssetPin())
	{
		if (const UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(TableAssetPin->DefaultObject))
		{
			return const_cast<UScriptStruct*>(TableAsset->RowStruct);
		}
	}

	const UEdGraphPin* TypedPin = GetTypedDataPin();
	if (!TypedPin)
	{
		return nullptr;
	}

	if (TypedPin->LinkedTo.Num() > 0)
	{
		for (const UEdGraphPin* LinkedPin : TypedPin->LinkedTo)
		{
			UScriptStruct* LinkedStruct = GetTypedDataPinContainerType() == EPinContainerType::Array
				? ExtractStructTypeFromArrayPin(LinkedPin)
				: ExtractStructTypeFromPin(LinkedPin);
			if (LinkedStruct)
			{
				return LinkedStruct;
			}
		}
	}

	return GetTypedDataPinContainerType() == EPinContainerType::Array
		? ExtractStructTypeFromArrayPin(TypedPin)
		: ExtractStructTypeFromPin(TypedPin);
}

void UK2Node_ESQLAsyncTypedResultBase::RefreshTypedDataPinType()
{
	SetTypedDataPinType(ResolveRowStructType());
}

void UK2Node_ESQLAsyncTypedResultBase::SetPinToolTip(UEdGraphPin& Pin, const FText& Description) const
{
	Pin.PinToolTip = UEdGraphSchema_K2::TypeToText(Pin.PinType).ToString();
	if (const UEdGraphSchema_K2* K2Schema = GetDefault<UEdGraphSchema_K2>())
	{
		Pin.PinToolTip += TEXT(" ");
		Pin.PinToolTip += K2Schema->GetPinDisplayName(&Pin).ToString();
	}
	Pin.PinToolTip += TEXT("\n");
	Pin.PinToolTip += Description.ToString();
}

void UK2Node_ESQLAsyncTypedResultBase::SetTypedDataPinType(UScriptStruct* NewRowStruct)
{
	UEdGraphPin* TypedPin = GetTypedDataPin();
	const UScriptStruct* OldStruct = GetTypedDataPinContainerType() == EPinContainerType::Array
		? ExtractStructTypeFromArrayPin(TypedPin)
		: ExtractStructTypeFromPin(TypedPin);
	if (OldStruct == NewRowStruct && TypedPin->PinType.ContainerType == GetTypedDataPinContainerType())
	{
		return;
	}

	TypedPin->PinType.ContainerType = GetTypedDataPinContainerType();
	TypedPin->PinType.PinCategory = NewRowStruct ? UEdGraphSchema_K2::PC_Struct : UEdGraphSchema_K2::PC_Wildcard;
	TypedPin->PinType.PinSubCategory = NAME_None;
	TypedPin->PinType.PinSubCategoryObject = NewRowStruct;
	CachedNodeTitle.Clear();
}

bool UK2Node_ESQLAsyncTypedResultBase::HandleDelegates(const TArray<FBaseAsyncTaskHelper::FOutputPinAndLocalVariable>& VariableOutputs, UEdGraphPin* ProxyObjectPin, UEdGraphPin*& InOutLastThenPin, UEdGraph* SourceGraph, FKismetCompilerContext& CompilerContext)
{
	const UEdGraphSchema_K2* Schema = CompilerContext.GetSchema();
	check(Schema);

	if (!ProxyFactoryClass || !ProxyClass)
	{
		CompilerContext.MessageLog.Error(*LOCTEXT("AsyncTypedMissingProxyClass", "@@ could not resolve the internal SQL async action class.").ToString(), this);
		return false;
	}

	const UESQLTableAsset* TableAsset = Cast<UESQLTableAsset>(GetTableAssetPin()->DefaultObject);
	if (!TableAsset)
	{
		CompilerContext.MessageLog.Error(*LOCTEXT("AsyncTypedHandleDelegateMissingTable", "@@ requires a literal SQL Table Asset for async typed result hydration.").ToString(), this);
		return false;
	}

	auto FindOutputPairByName = [&VariableOutputs](const FName PinName) -> const FBaseAsyncTaskHelper::FOutputPinAndLocalVariable*
	{
		for (const FBaseAsyncTaskHelper::FOutputPinAndLocalVariable& OutputPair : VariableOutputs)
		{
			if (OutputPair.OutputPin && OutputPair.OutputPin->PinName == PinName)
			{
				return &OutputPair;
			}
		}

		return nullptr;
	};

	const FBaseAsyncTaskHelper::FOutputPinAndLocalVariable* ResultPair = FindOutputPairByName(ESQLAsyncTypedK2NodePins::Result);
	const FBaseAsyncTaskHelper::FOutputPinAndLocalVariable* TypedPair = FindOutputPairByName(GetTypedDataPinName());
	if (!TypedPair)
	{
		CompilerContext.MessageLog.Error(*LOCTEXT("AsyncTypedHandleDelegateMissingTypedPair", "@@ failed to create a temporary typed async output variable.").ToString(), this);
		return false;
	}

	bool bIsErrorFree = true;
	for (TFieldIterator<FMulticastDelegateProperty> PropertyIt(ProxyClass); PropertyIt && bIsErrorFree; ++PropertyIt)
	{
		FMulticastDelegateProperty* CurrentProperty = *PropertyIt;
		UEdGraphPin* ExecPinForDelegate = FindPin(CurrentProperty->GetFName());
		if (!ExecPinForDelegate || ExecPinForDelegate->PinType.PinCategory != UEdGraphSchema_K2::PC_Exec)
		{
			CompilerContext.MessageLog.Error(*LOCTEXT("AsyncTypedMissingDelegateExecPin", "BaseAsyncTask: Cannot find execution pin for delegate @@").ToString(), this);
			return false;
		}

		UK2Node_CustomEvent* CurrentCENode = CompilerContext.SpawnIntermediateNode<UK2Node_CustomEvent>(this, SourceGraph);
		UK2Node_AddDelegate* AddDelegateNode = CompilerContext.SpawnIntermediateNode<UK2Node_AddDelegate>(this, SourceGraph);
		AddDelegateNode->SetFromProperty(CurrentProperty, false, CurrentProperty->GetOwnerClass());
		AddDelegateNode->AllocateDefaultPins();
		bIsErrorFree &= Schema->TryCreateConnection(AddDelegateNode->FindPinChecked(UEdGraphSchema_K2::PN_Self), ProxyObjectPin);
		bIsErrorFree &= Schema->TryCreateConnection(InOutLastThenPin, AddDelegateNode->FindPinChecked(UEdGraphSchema_K2::PN_Execute));
		InOutLastThenPin = AddDelegateNode->FindPinChecked(UEdGraphSchema_K2::PN_Then);

		CurrentCENode->CustomFunctionName = *FString::Printf(TEXT("%s_%s"), *CurrentProperty->GetName(), *CompilerContext.GetGuid(this));
		CurrentCENode->AllocateDefaultPins();
		bIsErrorFree &= FBaseAsyncTaskHelper::CreateDelegateForNewFunction(AddDelegateNode->GetDelegatePin(), CurrentCENode->GetFunctionName(), this, SourceGraph, CompilerContext);
		bIsErrorFree &= FBaseAsyncTaskHelper::CopyEventSignature(CurrentCENode, AddDelegateNode->GetDelegateSignature(), Schema);

		UEdGraphPin* CurrentThenPin = CurrentCENode->FindPinChecked(UEdGraphSchema_K2::PN_Then);
		for (const FBaseAsyncTaskHelper::FOutputPinAndLocalVariable& OutputPair : VariableOutputs)
		{
			if (!OutputPair.OutputPin || OutputPair.OutputPin->PinName == GetTypedDataPinName())
			{
				continue;
			}

			if (UEdGraphPin* PinWithData = CurrentCENode->FindPin(OutputPair.OutputPin->PinName))
			{
				CurrentThenPin = SpawnAssignmentNode(this, SourceGraph, CompilerContext, CurrentThenPin, OutputPair.TempVar->GetVariablePin(), PinWithData)->GetThenPin();
			}
		}

		if (CurrentProperty->GetFName() == ESQLAsyncTypedK2NodePins::OnSuccess)
		{
			UEdGraphPin* EventResultPin = CurrentCENode->FindPinChecked(ESQLAsyncTypedK2NodePins::Result);

			UK2Node_CallFunction* HydrationNode = CompilerContext.SpawnIntermediateNode<UK2Node_CallFunction>(this, SourceGraph);
			if (!ESQLRuntimeReflectionUtils::BindSQLBlueprintLibraryFunction(*HydrationNode, GetHydrationFunctionName()))
			{
				CompilerContext.MessageLog.Error(*LOCTEXT("AsyncTypedMissingHydrationLibraryClass", "@@ could not resolve the runtime SQL blueprint helper class.").ToString(), this);
				return false;
			}
			HydrationNode->AllocateDefaultPins();

			UEdGraphPin* HydrationTableAssetPin = HydrationNode->FindPinChecked(ESQLAsyncTypedK2NodePins::TableAsset);
			HydrationTableAssetPin->DefaultObject = const_cast<UESQLTableAsset*>(TableAsset);
			bIsErrorFree &= Schema->TryCreateConnection(EventResultPin, HydrationNode->FindPinChecked(ESQLAsyncTypedK2NodePins::Result));
			UEdGraphPin* HydratedValuePin = HydrationNode->FindPinChecked(GetTypedDataPinName());
			HydratedValuePin->PinType = TypedPair->OutputPin->PinType;
			bIsErrorFree &= Schema->TryCreateConnection(CurrentThenPin, HydrationNode->GetExecPin());

			CurrentThenPin = SpawnAssignmentNode(this, SourceGraph, CompilerContext, HydrationNode->GetThenPin(), TypedPair->TempVar->GetVariablePin(), HydratedValuePin)->GetThenPin();

			UK2Node_CallFunction* IsSuccessNode = CompilerContext.SpawnIntermediateNode<UK2Node_CallFunction>(this, SourceGraph);
			static const FName IsSQLQueryResultSuccessfulFunctionName(TEXT("IsSQLQueryResultSuccessful"));
			if (!ESQLRuntimeReflectionUtils::BindSQLBlueprintLibraryFunction(*IsSuccessNode, IsSQLQueryResultSuccessfulFunctionName))
			{
				CompilerContext.MessageLog.Error(*LOCTEXT("AsyncTypedMissingSuccessLibraryClass", "@@ could not resolve the runtime SQL blueprint helper class.").ToString(), this);
				return false;
			}
			IsSuccessNode->AllocateDefaultPins();
			bIsErrorFree &= Schema->TryCreateConnection(HydrationNode->GetReturnValuePin(), IsSuccessNode->FindPinChecked(TEXT("QueryResult")));

			UK2Node_IfThenElse* BranchNode = CompilerContext.SpawnIntermediateNode<UK2Node_IfThenElse>(this, SourceGraph);
			BranchNode->AllocateDefaultPins();
			bIsErrorFree &= Schema->TryCreateConnection(CurrentThenPin, BranchNode->GetExecPin());
			bIsErrorFree &= Schema->TryCreateConnection(IsSuccessNode->GetReturnValuePin(), BranchNode->GetConditionPin());

			if (ResultPair)
			{
				UK2Node_AssignmentStatement* FailureAssign = SpawnAssignmentNode(this, SourceGraph, CompilerContext, BranchNode->GetElsePin(), ResultPair->TempVar->GetVariablePin(), HydrationNode->GetReturnValuePin());
				bIsErrorFree &= CompilerContext.CopyPinLinksToIntermediate(*FindPinChecked(ESQLAsyncTypedK2NodePins::OnFailure), *FailureAssign->GetThenPin()).CanSafeConnect();
			}
			else
			{
				bIsErrorFree &= CompilerContext.CopyPinLinksToIntermediate(*FindPinChecked(ESQLAsyncTypedK2NodePins::OnFailure), *BranchNode->GetElsePin()).CanSafeConnect();
			}

			bIsErrorFree &= CompilerContext.CopyPinLinksToIntermediate(*ExecPinForDelegate, *BranchNode->GetThenPin()).CanSafeConnect();
		}
		else
		{
			bIsErrorFree &= CompilerContext.CopyPinLinksToIntermediate(*ExecPinForDelegate, *CurrentThenPin).CanSafeConnect();
		}
	}

	return bIsErrorFree;
}

